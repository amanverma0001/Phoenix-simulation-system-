// Helper function to get hidden truths for a country
export function getSanctionHiddenTruths(countryName: string): SanctionHiddenTruth | null {
    // Try exact match first
    if (sanctionHiddenTruths[countryName]) {
        return sanctionHiddenTruths[countryName];
    }

    // Try case-insensitive search
    const key = Object.keys(sanctionHiddenTruths).find(
        k => k.toLowerCase() === countryName.toLowerCase()
    );

    if (key) {
        return sanctionHiddenTruths[key];
    }

    // Try partial match for common variations
    const partialKey = Object.keys(sanctionHiddenTruths).find(
        k => k.toLowerCase().includes(countryName.toLowerCase()) ||
            countryName.toLowerCase().includes(k.toLowerCase())
    );

    return partialKey ? sanctionHiddenTruths[partialKey] : null;
}

// Get all hidden truths as array format for display
export function getHiddenTruthsArray(countryName: string): string[] {
    const truths = getSanctionHiddenTruths(countryName);
    if (!truths) return [];

    return [
        `💰 ${truths.economicDependency}`,
        `⚡ ${truths.powerStructure}`,
        `🏛️ ${truths.socialStability}`
    ];
}
